static err_t tcp_recv_callback(void *arg, struct tcp_pcb *tpcb, struct pbuf *p,
		err_t err) {
	if (p == NULL) {
		tcp_close(tpcb);
		return ERR_OK;
	}
	// 处理接收到的数据
	// 例如：回显
	tcp_write(tpcb, p->payload, p->len, TCP_WRITE_FLAG_COPY);
	tcp_recved(tpcb, p->len);
	pbuf_free(p);
	return ERR_OK;
}

// TCP连接回调函数
static err_t tcp_accept_callback(void *arg, struct tcp_pcb *newpcb, err_t err) {
	tcp_recv(newpcb, tcp_recv_callback);
	return ERR_OK;
}

// 初始化TCP服务器
void tcp_server_init(void) {
	struct tcp_pcb *pcb = tcp_new();
	if (pcb != NULL) {
		err_t err;
		err = tcp_bind(pcb, IP_ADDR_ANY, 5000); // 监听5000端口
		if (err == ERR_OK) {
			pcb = tcp_listen(pcb);
			tcp_accept(pcb, tcp_accept_callback);
		}
	}
}

// TCP客户端发送数据
err_t tcp_client_send(struct tcp_pcb *pcb, const void *data, u16_t len) {
	err_t err = tcp_write(pcb, data, len, TCP_WRITE_FLAG_COPY);
	if (err == ERR_OK) {
		tcp_output(pcb);
	}
	return err;
}
