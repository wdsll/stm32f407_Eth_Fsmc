/*******************************************************************************
** 函数名称:    SysVariableInit
** 函数功能:    初始化所有全局变量
** 形式参数:    无
** 返回参数:    无
*******************************************************************************/
void GlobalVarInit(void)
{
    InitAdcPara();
    InitCanVariable();//can
    //InitCanPara();//can
    InitSysPara();
    InitErrPara();
    InitEEPPara();
}

/*******************************************************************************
** 函数名称:    SysVariableInit
** 函数功能:    初始化所有外设
** 形式参数:    无
** 返回参数:    无
*******************************************************************************/
Uint16 gUSBInit = 0;
void DeviceInit(void)
{
    AdcInit();
    PwmInit();
    CAN_Init();         // CAN初始化
  //  CanInit();
    SciInit();
    LinInit();
    TimerInit();
    gUSBInit = USBInit();
}

